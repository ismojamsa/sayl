           <h4>1.  Mitä hyötyä minulle olisi SAYL:n jäsenyydestä?</h4>
           <p align="">Ystävyysseuran jäsenenä saat monipuolista tietoa Yhdysvalloista. 
           Kanavinamme ovat: Suomi-USA –jäsenlehti neljästi vuodessa, sähköinen USA-uutiskirje, 
           noin 200 eri puolilla Suomea järjestettävää Amerikka-tilaisuutta vuodessa ja 
           aktiivinen Facebook-ryhmä. Olet samalla tekemässä suomesta kansainvälisillä 
           markkinoilla pärjäävää maata. Lähetämme kymmeniä nuoria opiskeluvaihtoon ja 
           työharjoitteluun Yhdysvaltoihin. SAYL:n apurahasäätiöstä voit anoa apurahoja 
           jatko-opiskeluun Yhdysvalloissa. Summer Work and Travel –ohjelmamme tarjoaa 
           suomalaisnuorille kesätöitä Wisconsinissa. Englanninkieliset leikkikoulut ja 
           päiväkodit toimivat paikallisyhdistyksissä muutamilla paikkakunnilla. 
           Tule mukaan - verkostoidu, solmi uusia ystävyyksiä ja opi lisää Amerikasta.</p>
           <h4>2.  Olen joskus vuosia sitten ollut SAYL:n jäsen. Silloin matkustin edullisesti Amerikkaan. Vieläkö tämä jäsenetu on
           voimassa?</h4>
           <p>Ei. Suomen valmismatkalaki muuttui vuonna 1995 ja sen myötä nämä halvat lennot loppuivat. 
           SAYL:lla ei voittoa tuottamattomana ystävyysseurana ole mahdollisuuksia tallettaa Kuluttajavirastoon 
           nykyään vaadittavia takuita kaupallisten matkajärjestäjien lailla. Toisaalta hintakilpailu Atlantin 
           ylittävillä lennoilla on niin kova, että harva ostaisi lentolippuja entisen malliin, jopa vuotta 
           ennen reissua. Internet-kauppa on myös osaltaan muuttanut ihmisten matkailutottumuksia.
           Meillä on lentoyhtiö- ja matkatoimistokumppaneita, joiden kanssa järjestämme mm. erilaisia teemamatkoja 
           Yhdysvaltoihin. Toisinaan yhteistyö näkyy myös tarjouksina jäsekunnalle.</p>
